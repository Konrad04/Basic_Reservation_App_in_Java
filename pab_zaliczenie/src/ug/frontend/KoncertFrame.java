    private void bZarezerwuj_kActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bZarezerwuj_kActionPerformed
        // TODO add your handling code here:
        int Dostepne = Integer.parseInt(tBiletyDostepne_k.getText());
        int Rezerwowane = Integer.parseInt(tBiletyRezerwowane_k.getText());
        int Zaktualizowane=Dostepne- Rezerwowane;
        String newId = tEmail_k.getText()+tId_k.getText();
        if(Rezerwowane>0 && Dostepne>= Rezerwowane){
            bazaDanych.zarezerwuj(newId,Integer.parseInt(tId_k.getText()), 
                    tEmail_k.getText(), Rezerwowane,Zaktualizowane, Integer.parseInt(tId_k.getText()));
                 
            koncerty = bazaDanych.wszystkieKoncerty ();//przeładowanie po rezerwacji
       
        }
    }//GEN-LAST:event_bZarezerwuj_kActionPerformed
        
