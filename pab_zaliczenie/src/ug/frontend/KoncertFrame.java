    private void bSzukaj_kActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSzukaj_kActionPerformed
        // TODO add your handling code here:
        for(int indeks=0; indeks<koncerty.size(); indeks++){ //dla kazdego elementu z listy:
            koncert=(Koncert)koncerty.get(indeks); //tworzymy zmienną koncert do której przypisujemy element o typie Koncert
            koncert.getZespol_k();//pobieramy nazweZespolu kazdego elementu
            koncert.getMiejsce_k(); //pobieramy nazweMiejsca kazdego elementu
                if(tZespol_szukaj.getText().equals(koncert.getZespol_k()) && 
                        tMiejsce_szukaj.getText().equals(koncert.getMiejsce_k())){    
                            pokazKoncert(indeks);
            }   
        }
    }//GEN-LAST:event_bSzukaj_kActionPerformed

